
export default {
  OnLabel: function (e) {
    this.i = e;
    if (e == 0) {
      return  {Boxleft:0,i:e};
    }
    if (e == 1) {
      return  {Boxleft:-1122,i:e};
    }
    if (e == 2) {
      return  {Boxleft:-2244,i:e};
    }
    if (e == 3) {
      return  {Boxleft:-3366,i:e};
    }
    if (e == 4) {
      return  {Boxleft:-4488,i:e};
    }

  }
}
